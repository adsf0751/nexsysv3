/*
Function        :inHG_Func_ACCUM_UpdateFlow
Date&Time       :2017/02/07 下午 08:16
Describe        :做HG交易後計算總額的flow，回傳(VS_SUCCESS) or (VS_ERROR)
*/
int inHG_Func_ACCUM_UpdateFlow(TRANSACTION_OBJECT *pobTran)
{
        unsigned long           ulHandlePtr;            /* File Handle，type為pointer */
        int                     inRetVal;		/* return value，來判斷是否回傳error */
        unsigned char           uszFileName[14 + 1];    /* 儲存交易金額檔案的檔案名稱(最大為15) */
        HG_ACCUM_TOTAL_REC      srHGAccumRec;           /* 用來放總筆數、總金額的結構體 */
        
        /* inHG_Func_ACCUM_UpdateFlow()_START */
        if (ginDebug == VS_TRUE)
        {
                inLogPrintf(AT, "----------------------------------------");
                inLogPrintf(AT, "inHG_Func_ACCUM_UpdateFlow()_START");
        }
        
        if (inFunc_ComposeFileName(pobTran, (char*)uszFileName, _ACCUM_FILE_EXTENSION_, 6) != VS_SUCCESS)
	{
		/* Update Accum失敗 鎖機*/
		inFunc_EDCLock(AT);
			
		return (VS_ERROR);
	}
        
         /* 開檔 create檔(若沒有檔案則創建檔案) */
        if (inFILE_Open(&ulHandlePtr, uszFileName) == (VS_ERROR))
        {
                /* 開檔失敗時，不必關檔(開檔失敗，handle回傳NULL) */
                /* 若檔案不存在時，Create檔案 */
                if (inFILE_Check_Exist(uszFileName) == (VS_ERROR))
                {
                        inRetVal = inFILE_Create(&ulHandlePtr, uszFileName);
                }
                /* 檔案存在還是開檔失敗，回傳錯誤跳出 */
                else
                {
			/* Update Accum失敗 鎖機*/
			inFunc_EDCLock(AT);
			
                        return (VS_ERROR);
                }

                /* Create檔 */
                if (inRetVal != VS_SUCCESS)
                {
			/* Update Accum失敗 鎖機*/
			inFunc_EDCLock(AT);
		
                        /* Create檔失敗時不關檔，因為create檔失敗handle回傳NULL */
                        return (VS_ERROR);
                }
                /* create檔成功就繼續(因為create檔已經把handle指向檔案，所以不用在開檔) */
        }
        
        /* 開檔成功或create檔成功後 */
        /*先清空srAccumRec 為讀檔案作準備  */
        memset(&srHGAccumRec, 0x00, sizeof(srHGAccumRec));

        /* 把指針指到開頭*/
        inRetVal = inFILE_Seek(ulHandlePtr, 0, _SEEK_BEGIN_);
        
        /* inFile_seek */
        if (inRetVal != VS_SUCCESS)
        {
		/* Update Accum失敗 鎖機*/
		inFunc_EDCLock(AT);
		
                /* inFILE_Seek失敗時 */
                /* Seek檔失敗，所以關檔 */
                inFILE_Close(&ulHandlePtr);

                /* Seek檔失敗，所以回傳VS_ERROR。(關檔不論成功與否都要回傳(VS_ERROR)) */
                return (VS_ERROR);
        }
        
        /* 當文件內容為空時，read會回傳error;所以當read失敗， 而且檔案長度不等於0的時候，才是真的出錯 */
        if (inFILE_Read(&ulHandlePtr, (unsigned char *)&srHGAccumRec, _HG_ACCUM_REC_SIZE_) == VS_ERROR && lnFILE_GetSize(&ulHandlePtr, (unsigned char *) &srHGAccumRec) != 0)
        {
		/* Update Accum失敗 鎖機*/
		inFunc_EDCLock(AT);
		
                /* 讀檔失敗 */
                /* Read檔失敗，所以關檔 */
                inFILE_Close(&ulHandlePtr);

                /* Read檔失敗，所以回傳VS_ERROR。(關檔不論成功與否都要回傳(VS_ERROR)) */
                return (VS_ERROR);
        }
        else
        {
                if (pobTran->srBRec.uszVOIDBit == VS_TRUE)
                {
                        switch (pobTran->srBRec.lnHGTransactionType)
                        {
                                case _HG_REWARD_ :
					srHGAccumRec.uslnRewardAmount -= pobTran->srBRec.lnTxnAmount;
					srHGAccumRec.inRewardCnt --;
                                        break;
                                case _HG_ONLINE_REDEEM_ :
					srHGAccumRec.uslnOnlineRedeemAmount -= pobTran->srBRec.lnTxnAmount;
					srHGAccumRec.uslnOnlineRedeemPoint -= pobTran->srBRec.lnHGTransactionPoint;
					srHGAccumRec.inOnlineRedeemCnt --;
                                        break;
                                case _HG_POINT_CERTAIN_ :
                                        srHGAccumRec.uslnPointCertainAmount -= pobTran->srBRec.lnTxnAmount;
                                        srHGAccumRec.uslnPointCertainPoint -= pobTran->srBRec.lnHGTransactionPoint;
                                        srHGAccumRec.inPointCertainCnt --;
                                        break;
                                case _HG_FULL_REDEMPTION_ :
                                        srHGAccumRec.uslnFullRedemptionPoint -= pobTran->srBRec.lnHGTransactionPoint;
                                        srHGAccumRec.inFullRedemptionCnt --;
                                        break;
				case _HG_REWARD_REFUND_:
					srHGAccumRec.uslnRewardRefundPoint -= pobTran->srBRec.lnHGTransactionPoint;
					srHGAccumRec.inRewardRefundCnt --;
					break;
				case _HG_REDEEM_REFUND_:
					srHGAccumRec.uslnRedeemRefundPoint -= pobTran->srBRec.lnHGTransactionPoint;
					srHGAccumRec.inRedeemRefundCnt --;
					break;
                                default :
                                        return (VS_ERROR);
                        }
                }
                else
                {
                        switch (pobTran->srBRec.lnHGTransactionType)
                        {
                                case _HG_REWARD_ :
					srHGAccumRec.uslnRewardAmount += pobTran->srBRec.lnTxnAmount;
					srHGAccumRec.inRewardCnt ++;
                                        break;
                                case _HG_ONLINE_REDEEM_ :
					srHGAccumRec.uslnOnlineRedeemAmount += pobTran->srBRec.lnTxnAmount;
					srHGAccumRec.uslnOnlineRedeemPoint += pobTran->srBRec.lnHGTransactionPoint;
					srHGAccumRec.inOnlineRedeemCnt ++;
                                        break;
                                case _HG_POINT_CERTAIN_ :
                                        srHGAccumRec.uslnPointCertainAmount += pobTran->srBRec.lnTxnAmount;
                                        srHGAccumRec.uslnPointCertainPoint += pobTran->srBRec.lnHGTransactionPoint;
                                        srHGAccumRec.inPointCertainCnt ++;
                                        break;
                                case _HG_FULL_REDEMPTION_ :
                                        srHGAccumRec.uslnFullRedemptionPoint += pobTran->srBRec.lnHGTransactionPoint;
                                        srHGAccumRec.inFullRedemptionCnt ++;
                                        break;
				case _HG_REWARD_REFUND_:
					srHGAccumRec.uslnRewardRefundPoint += pobTran->srBRec.lnHGTransactionPoint;
					srHGAccumRec.inRewardRefundCnt ++;
					break;
				case _HG_REDEEM_REFUND_:
					srHGAccumRec.uslnRedeemRefundPoint += pobTran->srBRec.lnHGTransactionPoint;
					srHGAccumRec.inRedeemRefundCnt ++;
					break;
                                default :
                                        return (VS_ERROR);
                        }
                }
                /* 先將檔案關閉 */
                if (inFILE_Close(&ulHandlePtr) == (VS_ERROR))
                {
			/* Update Accum失敗 鎖機*/
			inFunc_EDCLock(AT);
			
                        /* 關檔失敗 */
                        /* 回傳VS_ERROR */
                        return (VS_ERROR);
                }

                /* 存檔案 */
                if (inHG_StoreRecord(&srHGAccumRec, uszFileName) == VS_ERROR)
                {
			/* Update Accum失敗 鎖機*/
			inFunc_EDCLock(AT);
		
                        /* 存檔失敗 */
                        /* 因為inACCUM_StoreRecord失敗，所以回傳VS_ERROR */
                        return (VS_ERROR);
                }
        }
        
        /* inHG_Func_ACCUM_UpdateFlow()_END */
        if (ginDebug == VS_TRUE)
        {
                inLogPrintf(AT, "----------------------------------------");
                inLogPrintf(AT, "inHG_Func_ACCUM_UpdateFlow()_END");
        }
        
        return (VS_SUCCESS);
}

