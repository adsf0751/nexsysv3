int inHG_RunTRT(TRANSACTION_OBJECT *pobTran, int inTRTCode) 
{
        int     *inTRTID = NULL;
        int     i, inRetVal = VS_ERROR ;

        for (i = 0 ;; i ++)
        {
                if (TRANSACTION_HG_TABLE[i].inTRTCode == inTRTCode)
                {
                        inTRTID = TRANSACTION_HG_TABLE[i].inTRTID;
                        break;
                }
                else if (TRANSACTION_HG_TABLE[i].inTRTCode == -1)
                        break;
        }

        if (inTRTID == NULL)
        {
                return (VS_ERROR);
        }

        for (i = 0 ; ; i ++)
        {
                if (inTRTID[i] == VS_ERROR)
                        break;

                inRetVal = inFLOW_RunFunction(pobTran, inTRTID[i]);

                if (inRetVal != VS_SUCCESS)
                {
                        break;
                }
        }

        /* 斷線 */
        inCOMM_End(pobTran);
	
        return (inRetVal);
}

